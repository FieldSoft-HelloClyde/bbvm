package me.wener.bbvm.neo.define;

public interface DataTypes
{
    /**
     * 32bit 和 INT 相同
     */
    public static final int T_DWORD = 0x0;
    /**
     * 16 bit
     */
    public static final int T_WORD = 0x1;
    /**
     * 8 bit
     */
    public static final int T_BYTE = 0x2;
    /**
     * 32 bit 浮点数
     */
    public static final int T_FLOAT = 0x3;
    /**
     * 32 bit 整数
     */
    public static final int T_INT = 0x4;
}
